number = int(input("Enter the Number  = "))
if 0 < number <= 10:
    if number == 1:
        print ("I\n")
    elif number == 2:
        print ("II\n")

    elif number == 3:
        print ("III\n")

    elif number == 4:
        print ("IV\n")

    elif number == 5:
        print ("V\n")

    elif number == 6:
        print ("VI\n")

    elif number == 7:
        print ("VII\n")

    elif number == 8:
        print ("VIII\n")

    elif number == 9:
        print ("IX\n")

    elif number == 10:
        print ("X\n")

else:
    print("Number Should be in Range from 1 to 10\n")



